    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
        if (CheckAlta.isSelected() != true && CheckBaja.isSelected() != true) {
            JOptionPane.showMessageDialog(this, "El tipo de movimiento no se ha seleccionado");
        } else if (TxtMatricula.getText().length() <= 0) {
            JOptionPane.showMessageDialog(this, "El campo de la matricula tiene que estar lleno");
        } else if (TxtGrupo.getText().length() <= 0) {
            JOptionPane.showMessageDialog(this, "El campo del grupo tiene que estar lleno");
        } else if (TxtProgramaEd.getText().length() <= 0) {
            JOptionPane.showMessageDialog(this, "El campo del programa educativo tiene que estar lleno");
        } else if (TxtMateria.getText().length() <= 0) {
            JOptionPane.showMessageDialog(this, "El campo de la materia tiene que estar lleno");
        } else {
            String ArchivoMateria = (String) ComboBox.getSelectedItem();
            Path path = Paths.get("src/segachafa/movimientos/" + ArchivoMateria);
            String TipoMovimiento;
            if (CheckAlta.isSelected()) {
                TipoMovimiento = "Alta";
            } else {
                TipoMovimiento = "Baja";
            }
            try {
                Files.delete(path);
            } catch (IOException ex) {
                Logger.getLogger(EliminarSolicitud.class.getName()).log(Level.SEVERE, null, ex);
            }

            String IdUnicoMateria = ArchivoMateria;
            Element childUUIDMovimiento = new Element("UUID");
            childUUIDMovimiento.addContent(UUID);
            Element Movimiento = new Element("Movimiento");
            Document docMovimientos = new Document();
            Element childMateria = new Element("Materia");
            childMateria.addContent(TxtMateria.getText());
            Element childGrupo = new Element("Grupo");
            childGrupo.addContent(TxtGrupo.getText());
            Element childTipoMov = new Element("TipoMovimiento");
            childTipoMov.addContent(TipoMovimiento);
            Element childObservaciones = new Element("Observaciones");
            childObservaciones.addContent(TxtObservaciones.getText());

            Movimiento.addContent(childUUIDMovimiento);
            Movimiento.addContent(childMateria);
            Movimiento.addContent(childGrupo);
            Movimiento.addContent(childTipoMov);
            Movimiento.addContent(childObservaciones);

            docMovimientos.setRootElement(Movimiento);
            XMLOutputter outter = new XMLOutputter();
            outter.setFormat(Format.getPrettyFormat());

            try {
                outter.output(docMovimientos, new FileWriter(new File("src/segachafa/movimientos/" + IdUnicoMateria)));
            } catch (IOException ex) {
                Logger.getLogger(CrearSolicitud.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Solicitud modificada con exito!");
        }
    }//GEN-LAST:event_BtnCrearActionPerformed

