    private void TxtMatriculaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtMatriculaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (TxtMatricula.getText().length() < 6 || TxtMatricula.getText().length() > 6) {
                JOptionPane.showMessageDialog(this, "La matricula tiene que ser de 6 digitos");
            } else {
                ComboBox.removeAllItems();

                File root = new File("src/segachafa/movimientos/");
                FilenameFilter beginswithm = new FilenameFilter() {
                    public boolean accept(File directory, String filename) {
                        return filename.startsWith(TxtMatricula.getText());
                    }
                };

                File[] files = root.listFiles(beginswithm);
                for (File f : files) {
                    String NombreArchivo = f.toString();
                    //ComboBox.addItem(NombreArchivo.substring(26, NombreArchivo.indexOf("@", 1)));
                    ComboBox.addItem(NombreArchivo.substring(26));
                }
                BtnAceptarAnidado.setEnabled(true);
                ComboBox.setEnabled(true);
            }
        }
    }//GEN-LAST:event_TxtMatriculaKeyPressed

