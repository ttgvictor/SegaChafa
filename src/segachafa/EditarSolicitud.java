    private void BtnAceptarAnidadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarAnidadoActionPerformed
        String ArchivoMateria = (String) ComboBox.getSelectedItem();
        SAXBuilder builder = new SAXBuilder();
        //System.out.println("prueba:   "+ "src/segachafa/movimientos/" + ArchivoMateria);
        File xmlFile = new File("src/segachafa/movimientos/" + ArchivoMateria);

        try {

            Document documentoXMLRead = (Document) builder.build(xmlFile);
            Element rootNode = documentoXMLRead.getRootElement();
            List list = rootNode.getChildren();

            for (int i = 0; i < list.size(); i++) {

                Element node = (Element) list.get(i);

                if (node.getName() == "Materia") {
                    TxtMateria.setEnabled(true);
                    TxtMateria.setText(node.getText());
                } else if (node.getName() == "Grupo") {
                    TxtGrupo.setEnabled(true);
                    TxtGrupo.setText(node.getText());
                } else if (node.getName() == "TipoMovimiento") {
                    if (node.getText() == "Alta") {
                        CheckAlta.setSelected(true);
                        CheckBaja.setSelected(false);
                    } else {
                        CheckBaja.setSelected(true);
                        CheckAlta.setSelected(false);
                    }
                } else if (node.getName() == "Observaciones") {
                    TxtObservaciones.setEnabled(true);
                    TxtObservaciones.setText(node.getText());
                } else if (node.getName() == "UUID") {
                    UUID = node.getText();
                } else {
                }
            }

        } catch (IOException io) {
            System.out.println(io.getMessage());
        } catch (JDOMException jdomex) {
            System.out.println(jdomex.getMessage());
        }

        //Aqui lee el de solicitud para obtener el programa educativo
        //System.out.println("ruta: " + "src/segachafa/solicitudes/" + ArchivoMateria.substring(0, 6) + "-" + ArchivoMateria.substring(ArchivoMateria.indexOf('%') + 1,ArchivoMateria.indexOf('@') +1) + ArchivoMateria.substring(ArchivoMateria.indexOf('@') + 1));
        File xmlFilePE = new File("src/segachafa/solicitudes/" + ArchivoMateria.substring(0, 6) + "-" + ArchivoMateria.substring(ArchivoMateria.indexOf('#') + 1));

        try {

            Document documentoXMLRead = (Document) builder.build(xmlFilePE);
            Element rootNode = documentoXMLRead.getRootElement();
            List list = rootNode.getChildren();

            for (int i = 0; i < list.size(); i++) {

                Element node = (Element) list.get(i);
                if (node.getName() == "ProgramaEducativo") {
                    TxtProgramaEd.setEnabled(true);
                    TxtProgramaEd.setText(node.getText());
                } else {
                }
            }

        } catch (IOException io) {
            System.out.println(io.getMessage());
        } catch (JDOMException jdomex) {
            System.out.println(jdomex.getMessage());
        }
        BtnAceptarAnidado.setEnabled(false);
        CheckAlta.setEnabled(true);
        CheckBaja.setEnabled(true);
        BtnCrear.setEnabled(true);
        TxtProgramaEd.setEnabled(false);
    }//GEN-LAST:event_BtnAceptarAnidadoActionPerformed

